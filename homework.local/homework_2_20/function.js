function sum(firstNum, secondNum){
    return alert(`Сумма равна ` + (firstNum + secondNum));
    
}

function sub(firstNum, secondNum){
    return alert(`Разность равна ` + (firstNum - secondNum));
}

function mult(firstNum, secondNum){
    return alert(`Произведение равно ` + (firstNum * secondNum));
}

function div(firstNum, secondNum){
    return alert(`Частное равно ` + (firstNum / secondNum));
}